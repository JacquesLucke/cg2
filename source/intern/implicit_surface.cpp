glm::vec3 interpolate(glm::vec3 pos1, glm::vec3 pos2, float value1, float value2) {
    if (std::abs(value1) < 0.00001) return pos1;
    if (std::abs(value2) < 0.00001) return pos2;
    if (std::abs(value1 - value2) < 0.00001) return pos1;

    float factor = value1 / (value1 - value2);
    return pos1 + factor * (pos2 - pos1);
}

void evaluateCell(
        float x0, float x1, float y0, float y1, float z0, float z1,
        std::array<float, 8> values, std::vector<glm::vec3> &positions)
{
    unsigned char cubeIndex = (
          ((values[0] < 0) << 0)
        | ((values[1] < 0) << 1)
        | ((values[2] < 0) << 2)
        | ((values[3] < 0) << 3)
        | ((values[4] < 0) << 4)
        | ((values[5] < 0) << 5)
        | ((values[6] < 0) << 6)
        | ((values[7] < 0) << 7)
    );

    int edges = edgeTable[cubeIndex];
    glm::vec3 edgePoints[12];

    if (edges == 0) {
        return;
    }
    if (edges & (1 <<  0)) edgePoints[ 0] = interpolate(glm::vec3(V0), glm::vec3(V1), values[0], values[1]);
    if (edges & (1 <<  1)) edgePoints[ 1] = interpolate(glm::vec3(V1), glm::vec3(V2), values[1], values[2]);
    if (edges & (1 <<  2)) edgePoints[ 2] = interpolate(glm::vec3(V2), glm::vec3(V3), values[2], values[3]);
    if (edges & (1 <<  3)) edgePoints[ 3] = interpolate(glm::vec3(V3), glm::vec3(V0), values[3], values[0]);
    if (edges & (1 <<  4)) edgePoints[ 4] = interpolate(glm::vec3(V4), glm::vec3(V5), values[4], values[5]);
    if (edges & (1 <<  5)) edgePoints[ 5] = interpolate(glm::vec3(V5), glm::vec3(V6), values[5], values[6]);
    if (edges & (1 <<  6)) edgePoints[ 6] = interpolate(glm::vec3(V6), glm::vec3(V7), values[6], values[7]);
    if (edges & (1 <<  7)) edgePoints[ 7] = interpolate(glm::vec3(V7), glm::vec3(V4), values[7], values[4]);
    if (edges & (1 <<  8)) edgePoints[ 8] = interpolate(glm::vec3(V0), glm::vec3(V4), values[0], values[4]);
    if (edges & (1 <<  9)) edgePoints[ 9] = interpolate(glm::vec3(V1), glm::vec3(V5), values[1], values[5]);
    if (edges & (1 << 10)) edgePoints[10] = interpolate(glm::vec3(V2), glm::vec3(V6), values[2], values[6]);
    if (edges & (1 << 11)) edgePoints[11] = interpolate(glm::vec3(V3), glm::vec3(V7), values[3], values[7]);

    for (int i = 0; triTable[cubeIndex][i] != -1; i += 3) {
        positions.push_back(edgePoints[triTable[cubeIndex][i + 0]]);
        positions.push_back(edgePoints[triTable[cubeIndex][i + 1]]);
        positions.push_back(edgePoints[triTable[cubeIndex][i + 2]]);
    }
}

inline int getValueIndex_XYZ(int x, int y, int z, int resolutionX, int resolutionY, int resolutionZ) {
    int xOffset = resolutionY * resolutionZ;
    int yOffset = resolutionZ;
    return x * xOffset + y * yOffset + z;
}

inline int getValueIndex_YZ(int y, int z, int resolutionY, int resolutionZ) {
    int yOffset = resolutionZ;
    return y * yOffset + z;
}

void evaluateImplicitSurface_Partial(
        ImplicitSurface &surface, BoundingBox<3> box, bool flipInAndOutside,
        int resolutionX, int resolutionY, int resolutionZ,
        int startX, int stepX, std::vector<float> &destination)
{
    float fResX = (float)resolutionX - 1.0f;
    float fResY = (float)resolutionY - 1.0f;
    float fResZ = (float)resolutionZ - 1.0f;

    for (int x = startX; x < resolutionX; x += stepX) {
        float _x = box.mapToBox(x / fResX, 0);
        std::cout << x << " " << std::flush;

        for (int y = 0; y < resolutionY; y++) {
            float _y = box.mapToBox(y / fResY, 1);

            for (int z = 0; z < resolutionZ; z++) {
                float _z = box.mapToBox(z / fResZ, 2);

                float value = surface.evaluate(_x, _y, _z);
                if (flipInAndOutside) value = -value;

                int index = getValueIndex_XYZ(x, y, z, resolutionX, resolutionY, resolutionZ);
                destination[index] = value;
            }
        }
    }
}

std::vector<float> evaluateImplicitSurface(
        ImplicitSurface &surface, BoundingBox<3> box, bool flipInAndOutside,
        int resolutionX, int resolutionY, int resolutionZ)
{
    std::vector<float> values;
    values.resize(resolutionX * resolutionY * resolutionZ);

    int threadCount = getCpuCoreCount() + 1;
    std::vector<std::thread> threads;

    for (int i = 0; i < threadCount; i++) {
        int startX = i;
        int stepX = threadCount;

        threads.push_back(
            std::thread(evaluateImplicitSurface_Partial,
                std::ref(surface), box, flipInAndOutside,
                resolutionX, resolutionY, resolutionZ,
                startX, stepX, std::ref(values))
        );
    }

    while (!threads.empty()) {
        threads.back().join();
        threads.pop_back();
    }

    std::cout << std::endl;
    return values;
}

